CameraFocus = 
{
	type = "CameraFocus",

	Editor=
	{
		Model="Objects/Editor/T.cgf",
	},
};

------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
function CameraFocus:OnInit()
	self:Activate(0);
end

function CameraFocus:OnReset()
	self:GotoState( "Inactive" );
end

------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
function CameraFocus:OnContact( Entity )
end

